export function searchLyrics(query: string): SongData[] {
  const normalizedQuery = query.toLowerCase().trim();
  return LYRICS_DB.filter(song => {
    const titleMatch = song.title.toLowerCase().includes(normalizedQuery);
    const aliasMatch = song.aliases.some(a => a.toLowerCase().includes(normalizedQuery));
    // Also check if query matches first line of lyrics
    const firstLine = song.lyrics.split('\n')[0].toLowerCase();
    const firstLineMatch = firstLine.includes(normalizedQuery);
    return titleMatch || aliasMatch || firstLineMatch;
  });
}

// Fuzzy search - find best matches even with partial input
export function fuzzySearchLyrics(query: string): SongData[] {
  const normalizedQuery = query.toLowerCase().trim();
  const words = normalizedQuery.split(/\s+/);

  const scored = LYRICS_DB.map(song => {
    let score = 0;
    const titleLower = song.title.toLowerCase();
    const allText = [song.title, ...song.aliases, song.lyrics.split('\n')[0]].join(' ').toLowerCase();

    // Exact title match
    if (titleLower === normalizedQuery) score += 100;
    // Title contains query
    if (titleLower.includes(normalizedQuery)) score += 50;
    // Alias match
    if (song.aliases.some(a => a.toLowerCase().includes(normalizedQuery))) score += 40;
    // Word matches - only count meaningful words (3+ chars)
    for (const word of words) {
      if (word.length < 3) continue;
      if (titleLower.includes(word)) score += 15;
      else if (song.aliases.some(a => a.toLowerCase().includes(word))) score += 12;
    }
    // Lyrics content match
    if (song.lyrics.toLowerCase().includes(normalizedQuery)) score += 20;

    return { song, score };
  });

  return scored
    .filter(s => s.score >= 30) // 최소 30점: 제목/별칭에 의미있는 매칭 필요
    .sort((a, b) => b.score - a.score)
    .map(s => s.song);
}
